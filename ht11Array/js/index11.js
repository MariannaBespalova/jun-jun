// 11. Дан трехмерный массив с числами, например [[[1, 2], [3, 4]], [[5, 6], [7, 8]]]. Найдите сумму элементов этого массива. Массив, конечно же, может быть произвольным.

let arr11 = [[[1, 2], [3, 4]], [[5, 6], [7, 8]]];

let sum11 = 0;

for (const i of arr11) {
  for (const j of i) {
    for (const n of j) {
      sum11 += n
    }
  }
}

console.log(sum11);